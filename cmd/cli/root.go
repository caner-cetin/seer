var cfg = &config.Config
var cfgFile string
var verbosity int

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use: "seer",
}
var versionCmd = &cobra.Command{
	Use: "version",
	Run: displayVersion,
}
var timeoutMs int

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

func init() {
	initConfig()

	// Here you will define your flags and configuration settings.
	// Cobra supports persistent flags, which, if defined here,
	// will be global for your application.

	rootCmd.SetHelpFunc(modifyHelp(rootCmd.HelpFunc()))
	rootCmd.PersistentFlags().StringVar(&cfgFile, "config", "", "config file (default is $HOME/.seer.yaml)")
	rootCmd.PersistentFlags().CountVarP(&verbosity, "verbose", "v", "verbose output (-v: info, -vv: debug, -vvv: trace)")
	rootCmd.Flags().BoolP("toggle", "t", false, "Help message for toggle")
	rootCmd.PersistentFlags().IntVarP(&timeoutMs, "timeout", "T", int((time.Minute * 1).Milliseconds()), "default timeout for commands in milliseconds, set to 1 minutes by default")
	rootCmd.AddCommand(server.GetRunCmd())
	rootCmd.AddCommand(getMigrateCmd())
	rootCmd.AddCommand(versionCmd)
	rootCmd.AddCommand(migrateCmd)
}

func modifyHelp(fn func(cmd *cobra.Command, args []string)) func(cmd *cobra.Command, args []string) {
	return func(cmd *cobra.Command, args []string) {
		if cfg.DisplayAsciiArtOnHelp {
			fmt.Println(arts[rand.IntN(len(arts))])
		}
		fn(cmd, args)
	}
}

// initConfig reads in config file and ENV variables if set.
func initConfig() {
	log.Logger = log.Output(zerolog.ConsoleWriter{Out: os.Stderr})
	switch verbosity {
	case 1:
		zerolog.SetGlobalLevel(zerolog.InfoLevel)
	case 2:
		zerolog.SetGlobalLevel(zerolog.DebugLevel)
	case 3:
		zerolog.SetGlobalLevel(zerolog.TraceLevel)
	default:
		zerolog.SetGlobalLevel(zerolog.WarnLevel)
	}

	var configPath string
	if cfgFile != "" {
		configPath = cfgFile
	} else {
		home, err := os.UserHomeDir()
		if err != nil {
			log.Error().Err(err).Msg("failed to get user home directory")
			return
		}
		configPath = filepath.Join(home, ".seer.yaml")
	}
	cfg.Path = configPath

	data, err := os.ReadFile(configPath)
	if err != nil {
		log.Error().Err(err).Msg("failed to read config")
		return
	}

	if err := yaml.Unmarshal(data, &config.Config); err != nil {
		log.Error().Err(err).Msg("error unmarshalling config")
		return
	}
	if err := config.SnapshotToDisk(); err != nil {
		log.Error().Err(err).Msg("failed to write config to disk")
		return
	}
}
func displayVersion(cmd *cobra.Command, args []string) {
	_, err := color.New(color.Bold).Printf("Seer %s \n", internal.Version)
	if err != nil {
		// how the fuck does this even throw error
		log.Error().Err(err).Send()
	}
}

